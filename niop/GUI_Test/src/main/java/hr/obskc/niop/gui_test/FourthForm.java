    private void formConfirmButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_formConfirmButtonMouseClicked
        String name = tagNameTextField.getText();
        String date = tagDateTextField.getText();

        if (name.isBlank() || date.isBlank()) {
            JOptionPane.showMessageDialog(this, "Morate unijeti sve podatke", "Gre≈°ka", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
            Tag tag = new Tag(name, sdf.parse(date));
            tags.add(tag);
            updateList();
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "Pogre≈°an oblik datuma, mora biti npr. 01.07.2025", "Gre≈°ka", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_formConfirmButtonMouseClicked

    private void deleteTagButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_deleteTagButtonMouseClicked
        System.out.println("test");
        Tag selected = tagsList.getSelectedValue();
        int selectedIndex = tagsList.getSelectedIndex();
        tagsListModel.remove(selectedIndex);
        tags.remove(selected);
    }//GEN-LAST:event_deleteTagButtonMouseClicked

    private void updateList() {
        tagsListModel.clear();
        for (Tag tag : tags) {
            tagsListModel.addElement(tag);
        }
    }

